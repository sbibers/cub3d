#include "../inc/libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*res;
	t_list	*node;
	void	*content;

	if (!lst || !f || !del)
		return (NULL);
	res = NULL;
	while (lst)
	{
		content = f(lst->content);
		node = ft_lstnew(content);
		if (!content || !node)
		{
			ft_lstclear(&res, del);
			if (!node)
				free (content);
			if (!content)
				free (node);
			return (NULL);
		}
		ft_lstadd_back (&res, node);
		lst = lst->next;
	}
	return (res);
}
